export function getClockEmoji(hour, minute) {
  const h = hour.toString().padStart(2, '0');
  if (minute >= 45) {
    const nextHour = ((hour + 1) % 24).toString().padStart(2, '0');
    return fullClocks[nextHour];
  } else if (minute >= 15 && minute < 45) {
    return halfClocks[h];
  } else {
    return fullClocks[h];
  }
}
